// ─── Test Suite ───────────────────────────────────────────────────────────────

describe('Execution Loop E2E', () => {
  let orchestratorService: OrchestratorService;
  let agentJobRunner: AgentJobRunner;
  let planRepo: ReturnType<typeof createMockPlanRepo>;
  let executionRepo: ReturnType<typeof createMockExecutionRepo>;
  let eventsGateway: any;
  let queueProvider: InMemoryQueueAdapter;
  let agentRegistry: AgentRegistry;

  beforeEach(async () => {
    const vibeConfig = loadVibeConfig();
    queueProvider = new InMemoryQueueAdapter();
    const cache = new InMemoryCacheAdapter();
    planRepo = createMockPlanRepo();
    executionRepo = createMockExecutionRepo();
    const contextRepo = createMockContextRepo();

    // Agent registry with mock agents
    agentRegistry = new AgentRegistry();
    agentRegistry.register(createMockAgent('analyst', 'analyst', ['analysis']));
    agentRegistry.register(
      createMockAgent('architect', 'architect', ['architecture']),
    );
    agentRegistry.register(
      createMockAgent('coder', 'coder', ['code-generation']),
    );
    agentRegistry.register(createMockAgent('tester', 'tester', ['testing']));

    // Context compiler (uses mock repos)
    const contextCompiler = new ContextCompiler(
      contextRepo as any,
      cache,
      vibeConfig,
    );

    // Mock events gateway
    eventsGateway = {
      emitExecutionEvent: jest.fn(),
      emitWorkerStatusUpdate: jest.fn(),
    };

    // Mock VCS provider
    const mockVcs = {
      createBranch: jest.fn().mockResolvedValue(undefined),
      checkout: jest.fn().mockResolvedValue(undefined),
      commit: jest.fn().mockResolvedValue(undefined),
      push: jest.fn().mockResolvedValue(undefined),
      getStatus: jest.fn().mockResolvedValue({ modified: [], untracked: [] }),
      getCurrentBranch: jest.fn().mockResolvedValue('main'),
      getDiff: jest.fn().mockResolvedValue(''),
      getLog: jest.fn().mockResolvedValue([]),
      mergeBranch: jest.fn().mockResolvedValue(undefined),
    };

    // Mock Planner that creates a deterministic 3-node DAG
    const mockPlanner = {
      createPlan: jest.fn(async (intent: string, projectId: string) => {
        const dag: DAGNode[] = [
          {
            nodeId: 'n1',
            agentId: 'analyst',
            taskDefinition: {
              id: 't1',
              type: 'analysis' as any,
              description: 'Analyze requirements',
              tags: ['backend'],
              dependencies: [],
              priority: 1,
              timeoutMs: 60000,
            },
            dependencies: [],
            status: 'pending' as any,
          },
          {
            nodeId: 'n2',
            agentId: 'coder',
            taskDefinition: {
              id: 't2',
              type: 'code-generation' as any,
              description: 'Generate code',
              tags: ['backend'],
              dependencies: ['t1'],
              priority: 1,
              timeoutMs: 60000,
            },
            dependencies: ['n1'],
            status: 'pending' as any,
          },
          {
            nodeId: 'n3',
            agentId: 'tester',
            taskDefinition: {
              id: 't3',
              type: 'testing' as any,
              description: 'Run tests',
              tags: ['backend'],
              dependencies: ['t2'],
              priority: 1,
              timeoutMs: 60000,
            },
            dependencies: ['n2'],
            status: 'pending' as any,
          },
        ];

        const plan = await planRepo.create({
          projectId,
          intent,
          dag,
          estimatedCost: 0.003,
          estimatedDuration: 180000,
          status: 'pending_approval',
        });
        return plan;
      }),
    };

    // Mock DecisionCache, CostTracker, PromptCompiler
    const mockDecisionCache = {
      getCachedDecision: jest.fn().mockResolvedValue(null),
      cacheDecision: jest.fn(),
      buildKey: jest.fn().mockReturnValue('key'),
      computeContextHash: jest.fn().mockReturnValue('hash'),
    };

    const mockCostTracker = {
      checkBudget: jest.fn().mockResolvedValue({
        allowed: true,
        remaining: 10,
        currentSpend: 0,
        budgetLimit: 10,
      }),
      trackCost: jest.fn().mockResolvedValue(undefined),
    };

    const mockPromptCompiler = {
      compileSystemPrompt: jest.fn().mockReturnValue('system prompt'),
      registerModule: jest.fn(),
      getModuleCount: jest.fn().mockReturnValue(0),
    };

    // Mock QualityGatesService
    const mockQualityGates = {
      runLintGate: jest.fn().mockResolvedValue({ passed: true, issues: [] }),
      runSecurityGate: jest
        .fn()
        .mockResolvedValue({ passed: true, issues: [] }),
      runTestGate: jest.fn().mockResolvedValue({ passed: true, issues: [] }),
    };

    const resultEvaluator = new ResultEvaluator(
      mockQualityGates as any,
      vibeConfig,
    );

    const scheduler = new Scheduler(
      queueProvider,
      planRepo as any,
      mockVcs as any,
    );

    const workerPoolManager = new WorkerPoolManager(
      queueProvider,
      cache,
      agentRegistry,
      vibeConfig,
    );

    const traceContext = new TraceContext();

    orchestratorService = new OrchestratorService(
      mockPlanner as any,
      scheduler,
      resultEvaluator,
      traceContext,
      eventsGateway,
      mockDecisionCache as any,
      mockCostTracker as any,
      mockPromptCompiler as any,
      planRepo as any,
    );

    agentJobRunner = new AgentJobRunner(
      queueProvider,
      agentRegistry,
      contextCompiler,
      orchestratorService,
      executionRepo as any,
      workerPoolManager,
    );

    // Initialize — registers handlers for all agents
    await agentJobRunner.onModuleInit();
  });

  it('should execute the full pipeline from intent to completion', async () => {
    // Step 1: Create plan
    const plan = await orchestratorService.executeIntent(
      'Create a hello world app',
      'proj-1',
      { apiKey: 'test-key' },
    );
    const planId = plan._id?.toString() ?? (plan as any).id;

    expect(planId).toBeDefined();
    expect(plan.dag).toHaveLength(3);
    expect(plan.status).toBe('pending_approval');

    // Step 2: Approve plan → triggers dispatch → triggers agent execution
    await orchestratorService.approvePlan(planId);

    // Allow async processing to complete
    await new Promise((resolve) => setTimeout(resolve, 200));

    // Step 3: Verify all agents executed
    const finalPlan = planRepo._plans.get(planId);
    expect(finalPlan).toBeDefined();
    expect(finalPlan.status).toBe('completed');

    // All DAG nodes should be completed
    for (const node of finalPlan.dag) {
      expect(node.status).toBe('completed');
    }
  });

  it('should create AgentExecution records for each agent', async () => {
    const plan = await orchestratorService.executeIntent(
      'Build an API',
      'proj-1',
      { apiKey: 'test-key' },
    );
    const planId = (plan as any)._id ?? (plan as any).id;

    await orchestratorService.approvePlan(planId);
    await new Promise((resolve) => setTimeout(resolve, 200));

    // Should have 3 execution records (one per agent)
    expect(executionRepo.create).toHaveBeenCalledTimes(3);

    // Each should start as 'running'
    for (const call of executionRepo.create.mock.calls) {
      expect(call[0].status).toBe('running');
    }

    // Each should be updated to 'completed'
    const completedUpdates = executionRepo.update.mock.calls.filter(
      (call: any[]) => call[1].status === 'completed',
    );
    expect(completedUpdates).toHaveLength(3);
  });

  it('should emit WebSocket events for pipeline lifecycle', async () => {
    const plan = await orchestratorService.executeIntent(
      'Create project',
      'proj-1',
      { apiKey: 'test-key' },
    );
    const planId = (plan as any)._id ?? (plan as any).id;

    await orchestratorService.approvePlan(planId);
    await new Promise((resolve) => setTimeout(resolve, 200));

    const emittedEvents = eventsGateway.emitExecutionEvent.mock.calls.map(
      (call: any[]) => call[1].type,
    );

    expect(emittedEvents).toContain('plan_created');
    expect(emittedEvents).toContain('execution_started');
    expect(emittedEvents).toContain('node_completed');
    expect(emittedEvents).toContain('pipeline_completed');
  });

  it('should execute DAG nodes in dependency order', async () => {
    const executionOrder: string[] = [];

    // Override mock agents to track execution order
    const trackedAgents = ['analyst', 'coder', 'tester'];
    for (const id of trackedAgents) {
      const agent = agentRegistry.get(id);
      const originalExecute = agent.execute.bind(agent);
      (agent as any).execute = async (input: AgentInput) => {
        executionOrder.push(id);
        return originalExecute(input);
      };
    }

    const plan = await orchestratorService.executeIntent(
      'Build sequential app',
      'proj-1',
      { apiKey: 'test-key' },
    );
    const planId = (plan as any)._id ?? (plan as any).id;

    await orchestratorService.approvePlan(planId);
    await new Promise((resolve) => setTimeout(resolve, 200));

    // Analyst must run before coder, coder before tester
    expect(executionOrder).toEqual(['analyst', 'coder', 'tester']);
  });

  it('should handle agent failure and mark plan as failed', async () => {
    // Make coder agent fail
    const coderAgent = agentRegistry.get('coder');
    (coderAgent as any).execute = async () => {
      throw new Error('LLM rate limit exceeded');
    };

    const plan = await orchestratorService.executeIntent(
      'Build failing app',
      'proj-1',
      { apiKey: 'test-key' },
    );
    const planId = (plan as any)._id ?? (plan as any).id;

    await orchestratorService.approvePlan(planId);
    await new Promise((resolve) => setTimeout(resolve, 200));

    const finalPlan = planRepo._plans.get(planId);
    expect(finalPlan.status).toBe('failed');

    // Analyst should have completed, coder failed, tester skipped
    const analystNode = finalPlan.dag.find((n: any) => n.agentId === 'analyst');
    const coderNode = finalPlan.dag.find((n: any) => n.agentId === 'coder');
    const testerNode = finalPlan.dag.find((n: any) => n.agentId === 'tester');

    expect(analystNode.status).toBe('completed');
    expect(coderNode.status).toBe('failed');
    expect(testerNode.status).toBe('skipped');

    // Failure execution record should exist
    const failedUpdates = executionRepo.update.mock.calls.filter(
      (call: any[]) => call[1].status === 'failed',
    );
    expect(failedUpdates).toHaveLength(1);
    expect(failedUpdates[0][1].errorDetails.message).toBe(
      'LLM rate limit exceeded',
    );
  });
});
