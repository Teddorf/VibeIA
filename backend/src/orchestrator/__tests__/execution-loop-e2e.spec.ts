// ─── Mock Agent Factory ───────────────────────────────────────────────────────

function createMockAgent(
  id: string,
  role: string,
  taskTypes: string[],
): IAgent {
  return {
    profile: {
      id,
      name: `${id.charAt(0).toUpperCase() + id.slice(1)}Agent`,
      role,
      capabilities: taskTypes,
      defaultModel: 'fast' as any,
      systemPromptTemplate: '',
    } as AgentProfile,

    async execute(input: AgentInput): Promise<AgentOutput> {
      return {
        taskId: input.taskDefinition.id,
        agentId: id,
        status: 'success',
        artifacts: [
          {
            type: 'code',
            path: `${id}-output.ts`,
            content: `// Generated by ${id} for ${input.taskDefinition.description}`,
          },
        ],
        contextUpdates: [],
        metrics: {
          startedAt: new Date(),
          durationMs: 50,
          tokensUsed: 100,
          costUSD: 0.001,
          llmCalls: 1,
          retries: 0,
        },
        traceId: input.traceId,
      };
    },

    validateInput(_input: AgentInput): ValidationError[] | null {
      return null;
    },

    estimateCost(_input: AgentInput): CostEstimate {
      return {
        estimatedTokens: 100,
        estimatedCostUSD: 0.001,
        tier: 'fast' as any,
      };
    },

    canHandle(task: TaskDefinition): boolean {
      return taskTypes.includes(task.type);
    },
  };
}

// ─── Mock Repositories ────────────────────────────────────────────────────────

function createMockPlanRepo() {
  const plans = new Map<string, any>();
  let counter = 0;

  return {
    create: jest.fn(async (data: any) => {
      const id = `plan-${++counter}`;
      const plan = { ...data, _id: id, id };
      plans.set(id, plan);
      return plan;
    }),
    findById: jest.fn(async (id: string) => {
      const plan = plans.get(id);
      return plan ? { ...plan } : null;
    }),
    update: jest.fn(async (id: string, data: any) => {
      const plan = plans.get(id);
      if (!plan) return null;
      Object.assign(plan, data);
      plans.set(id, plan);
      return plan;
    }),
    find: jest.fn(async () => []),
    findOne: jest.fn(async () => null),
    delete: jest.fn(async () => true),
    count: jest.fn(async () => plans.size),
    findOneAndUpdate: jest.fn(async () => null),
    findOneAndDelete: jest.fn(async () => null),
    updateMany: jest.fn(async () => ({ modifiedCount: 0 })),
    deleteMany: jest.fn(async () => ({ deletedCount: 0 })),
    insertMany: jest.fn(async () => []),
    findMany: jest.fn(async () => []),
    createMany: jest.fn(async () => []),
    exists: jest.fn(async () => false),
    // IExecutionPlanRepository methods
    findByProjectAndStatus: jest.fn(async () => []),
    updateNodeStatus: jest.fn(async () => null),
    getReadyNodes: jest.fn(async (planId: string) => {
      const plan = plans.get(planId);
      if (!plan) return [];
      return plan.dag
        .filter(
          (n: any) =>
            n.status === 'pending' &&
            n.dependencies.every((dep: string) => {
              const depNode = plan.dag.find((d: any) => d.nodeId === dep);
              return depNode && depNode.status === 'completed';
            }),
        )
        .map((n: any) => ({ nodeId: n.nodeId, agentId: n.agentId }));
    }),
    _plans: plans,
  };
}

function createMockExecutionRepo() {
  const executions: any[] = [];

  return {
    create: jest.fn(async (data: any) => {
      const id = `exec-${executions.length + 1}`;
      const exec = { ...data, _id: id, id };
      executions.push(exec);
      return exec;
    }),
    update: jest.fn(async (id: string, data: any) => {
      const exec = executions.find((e) => e._id === id);
      if (exec) Object.assign(exec, data);
      return exec;
    }),
    find: jest.fn(async (filter: any) => {
      return executions.filter((e) => {
        if (filter.pipelineId && e.pipelineId !== filter.pipelineId)
          return false;
        if (filter.status && e.status !== filter.status) return false;
        return true;
      });
    }),
    findById: jest.fn(async () => null),
    findOne: jest.fn(async () => null),
    delete: jest.fn(async () => true),
    count: jest.fn(async () => executions.length),
    findOneAndUpdate: jest.fn(async () => null),
    findOneAndDelete: jest.fn(async () => null),
    updateMany: jest.fn(async () => ({ modifiedCount: 0 })),
    deleteMany: jest.fn(async () => ({ deletedCount: 0 })),
    insertMany: jest.fn(async () => []),
    findMany: jest.fn(async () => []),
    createMany: jest.fn(async () => []),
    exists: jest.fn(async () => false),
    // IAgentExecutionRepository methods
    getMetricsForPipeline: jest.fn(async () => ({
      totalExecutions: 0,
      completedExecutions: 0,
      failedExecutions: 0,
      totalCostUSD: 0,
      totalTokensUsed: 0,
      averageDurationMs: 0,
    })),
    getCostForProject: jest.fn(async () => 0),
    _executions: executions,
  };
}

